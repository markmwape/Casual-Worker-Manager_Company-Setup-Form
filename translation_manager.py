def get_translation(text, language_code='en'):
    """Get translation for a text"""
    if language_code == 'en':
        return text
    
    if text in TRANSLATIONS:
        return TRANSLATIONS[text].get(language_code, text)
    
    return text


def export_translations_to_json():
    """Export translations to JSON files for frontend use"""
    output_dir = Path(__file__).parent / 'static' / 'translations'
    output_dir.mkdir(parents=True, exist_ok=True)
    
    languages = ['en', 'fr', 'sw', 'pt', 'es', 'tr', 'hi', 'zh', 'ar', 'vi']
    
    for lang in languages:
        translations = {}
        for english_text, trans_dict in TRANSLATIONS.items():
            if lang == 'en':
                translations[english_text] = english_text
            else:
                translations[english_text] = trans_dict.get(lang, english_text)
        
        output_file = output_dir / f'{lang}.json'
        with open(output_file, 'w', encoding='utf-8') as f:
            json.dump(translations, f, ensure_ascii=False, indent=2)
        
        print(f"✅ Exported {lang}.json")


if __name__ == '__main__':
    export_translations_to_json()
    print("\n✅ All translations exported successfully!")
